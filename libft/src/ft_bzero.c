#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptr;

	ptr = s;
	i = 0;
	if (n)
		while (i < n)
		{
			ptr[i] = 0;
			i++;
		}
}
